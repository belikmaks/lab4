def student5_obrobka(text): #функція обробки від п'ятого студента
    new_text_len=len(text)#визначення кількості символів у заданому тексті
    print("\nДовжина тексту до обробки п'ятим студентом: ", new_text_len)
    new_text=text.center(new_text_len+6,"*")#вирівнювання тексту по середині за допомогою заданого символу "*"
    new_text=new_text.replace(" ", "\t")#функція заміни використана попередньо, тут заміняє пробіли знаками табуляції
    new_text=new_text.expandtabs(4)#заміна знаків табуляції на пробіли, їх кількість вказана в дужках
    print("Текст після обробки п'ятим студентом: ")
    print(new_text)


